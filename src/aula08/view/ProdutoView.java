    private void btInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirActionPerformed
        
        ProdutoModel modelo = new ProdutoModel();
        modelo.setDescricao(tfDescricao.getText());
        modelo.setValor(Float.parseFloat(tfValor.getText()));
        modelo.setEstoque(Integer.parseInt(tfEstoque.getText()));
        if(produtoController.inserir(modelo)) {
            JOptionPane.showMessageDialog(null, "Produto inserido com sucesso!");
        }
    }//GEN-LAST:event_btInserirActionPerformed

